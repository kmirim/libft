#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;

	str = (char *)s;
	while (n-- > 0)
		str[n] = 0;
}

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;

	if ((size) && (nmemb)
		&& (nmemb * size) / size != nmemb)
		return (NULL);
	ptr = malloc(nmemb * size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, nmemb * size);
	return (ptr);
}
